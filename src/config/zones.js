const generateMapData = (width, height, fillValue, borderValue) => {
  const map = [];
  for (let y = 0; y < height; y++) {
    // Create a new row array
    const row = new Array(width);

    for (let x = 0; x < width; x++) {
      // Check if we are on the perimeter (first or last row/column)
      if (y === 0 || y === height - 1 || x === 0 || x === width - 1) {
        row[x] = borderValue;
      } else {
        row[x] = fillValue;
      }
    }
    map.push(row);
  }
  return map;
};

const generateEnemyData = (xMin, yMin, xMax, yMax, numEnemies) => {
  const enemies = [];
  for (let i = 0; i < numEnemies; i++) {
    const x = Math.floor(Math.random() * (xMax - xMin + 1)) + xMin;
    const y = Math.floor(Math.random() * (yMax - yMin + 1)) + yMin;
    enemies.push({ x, y });
  }
  return enemies;
};

